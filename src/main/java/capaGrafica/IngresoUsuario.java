    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed

   try {
        int usuario = Integer.parseInt(cedulausuario.getText());

        String cedusuario = cedulausuario.getText();

        if (cedusuario.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No puede ingresar campos vacios.");
            
        }else {
            
            FachadaLogica fachada = new FachadaLogica();
            
            if(fachada.IniciarUs(usuario)){
            JOptionPane.showMessageDialog(null, "Bienvenido ");
            dispose();
            setVisible(false);
            TablaDeDatos tabla =new TablaDeDatos();
            tabla.setVisible(true);
            }
            else{
            JOptionPane.showMessageDialog(null, "La cedula que ingreso no es correcta, intentelo de nuevo mas tarde.");
            }
            limpiar();
        }
        }catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error al iniciar sesi√≥n: " + ex.getMessage());
        }
    }//GEN-LAST:event_IngresarActionPerformed

